# Create Array
fruits = ["Pineapple", "Banana", "Apple", "Melon"]
# add an element
fruits.append("Kiwi")
print(fruits)
# add an element on specific location
fruits.insert(0, "Orange")
print(fruits)
# Remove in elements
fruits.remove("Banana")
print(fruits)
# Remove by pop
fruits.pop(2)
print(fruits)
# Change element or edit
fruits[0] = "Mango"
print(fruits)